const express = require('express');
const cors = require('cors');
const dotenv = require('dotenv');
const { orderRouter } = require('./routes/order.routes');
const { tradeRouter } = require('./routes/trade.routes');
const { marketRouter } = require('./routes/market.routes');
const { errorHandler } = require('./middleware/errorHandler');
const { initializeWebSocket } = require('./events/websocket');
const { initializeMatchingEngine } = require('./matching-engine/matchingEngine');
const { initializeComplianceService } = require('./compliance/complianceService');

dotenv.config();

const app = express();
const PORT = process.env.PORT || 3001;

app.use(cors());
app.use(express.json());

// Health check
app.get('/health', (req, res) => {
  res.json({ 
    status: 'ok', 
    timestamp: new Date().toISOString(),
    uptime: process.uptime(),
    environment: process.env.NODE_ENV || 'development'
  });
});

// API Routes
app.use('/api/orders', orderRouter);
app.use('/api/trades', tradeRouter);
app.use('/api/market', marketRouter);

// Error handling
app.use(errorHandler);

// Initialize services
initializeMatchingEngine();
initializeComplianceService();

const server = app.listen(PORT, () => {
  console.log(`🚀 Backend server running on port ${PORT}`);
});

// Initialize WebSocket
initializeWebSocket(server);

module.exports = app;
